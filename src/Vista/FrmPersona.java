    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        
        
        if(txtRut.getText().trim().length() < 1 || txtDigito.getText().trim().length() <1 || txtNombre.getText().trim().length() <1 || txtApellido.getText().trim().length() <1)
        {
            JOptionPane.showMessageDialog(this, "Ingrese todos los datos antes de continuar");
            txtRut.requestFocus();
        }
        else
        {   // guardar a la base de datos si están toda la información
             
            if (txtDigito.getText().trim().length() > 1 || isNumeric(txtDigito.getText()) == false && ! txtDigito.getText().trim().equalsIgnoreCase("k")) 
                {
            JOptionPane.showMessageDialog(this, "El digito verificador solo puede ser un caracter y no puede ser letras que no sea K");
            txtRut.requestFocus();
                }
            else if (isNumeric(txtRut.getText()) == false)
                {
            JOptionPane.showMessageDialog(this, "El rut tiene que ser numeros");
            txtRut.requestFocus();
        }
            else if (Integer.parseInt(txtSueldo.getText()) < 400000 || Integer.parseInt(txtSueldo.getText()) > 2500000)
                {
                    JOptionPane.showMessageDialog(this, "El sueldo no puede ser menor a 400.000 o mayor a 2.500.000");
            txtRut.requestFocus();
                }
            else if(txtNombre.getText().trim().length() > 50 || txtApellido.getText().trim().length() > 50) {
                JOptionPane.showMessageDialog(this, "El nombre y el apellido no pueden superar los 50 caracteres");
            }
           else {
                Persona persona = new Persona() {};
                Especialidad especialidad = new Especialidad(){};
                Cargo cargo = new Cargo(){};
                Comuna comuna = new Comuna(){};
                
                if(txtId.getText().equals("")) {
                    
                }
                else {
                    persona.setIdPersona(Integer.parseInt(txtId.getText().trim().toUpperCase()));
                }
                if(txtSueldo.getText().equals(""))
                {
                    persona.setSueldo(0);
                }
                else{
                    persona.setSueldo(Integer.parseInt(txtSueldo.getText().trim().toUpperCase()));
                }
                especialidad.setIdEspecialidad(Integer.parseInt(jEspecialidad.getSelectedItem().toString().substring(0, jEspecialidad.getSelectedItem().toString().indexOf(" "))));
                cargo.setIdCargo(Integer.parseInt(jCargo.getSelectedItem().toString().substring(0, jCargo.getSelectedItem().toString().indexOf(" "))));
                comuna.setIdComuna(Integer.parseInt(jComuna.getSelectedItem().toString().substring(0, jComuna.getSelectedItem().toString().indexOf(" "))));
                persona.setDigito(txtDigito.getText().trim().toUpperCase());
                persona.setRut(Integer.parseInt(txtRut.getText().trim().toUpperCase()));
                persona.setNombre(txtNombre.getText().trim().toUpperCase());
                persona.setApellido(txtApellido.getText().trim().toUpperCase());
                persona.setEsCliente(radioCliente.isSelected());
                persona.setHabilitado(chkHabilitado.isSelected());
                ControladorPersona cm = new ControladorPersona();
                boolean resultado =  false;
                if(txtId.getText().equals(""))
                resultado = cm.agregar(persona, cargo,especialidad,comuna);
                else
                resultado = cm.actualizar(persona, cargo,especialidad,comuna);

            if(resultado)
            {
                JOptionPane.showMessageDialog(this, "Datos Guardados");
                btnLimpiar.doClick();
                btnListar.doClick();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Error en la operación");
            }
            
            

            
        }

    }//GEN-LAST:event_btnGuardarActionPerformed
    }
