    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        if(txtId.getText().trim().length()<1)
        {
            JOptionPane.showMessageDialog(this, "No especifico el id");
            txtId.requestFocus();
        }
        else
        {
            int id = Integer.parseInt(txtId.getText());
            ControladorPersona cm = new ControladorPersona();
            Persona m = cm.buscarPorId(id);
            if(m == null)
            {
                JOptionPane.showMessageDialog(this, "id no existe");
                btnLimpiar.doClick();
                txtId.requestFocus();
            }
            else
            {
                persona.setIdPersona(m.getIdPersona());
                txtRut.setText(Integer.toString(m.getRut()));
                txtDigito.setText(m.getDigito());
                txtApellido.setText(m.getApellido());
                txtNombre.setText(m.getNombre());
                txtSueldo.setText(Integer.toString(m.getSueldo()));
                
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

